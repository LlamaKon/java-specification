package Third;

public class Sample3 {

    public static void main(String[] args) {

        // 標準出力

        // 整数値を入力
        int num = 2;

        // numが１の場合
        if (num == 1) {
            System.out.println("one");
        }

        // numが２の場合
        else if (num == 2) {
            System.out.println("two");
        }

        // numが３の場合
        else if (num == 3) {
            System.out.println("three");
        }

        // それ以外の値が入力された場合
        else {
            System.out.println("不適切な値です。");
        }
    }
}

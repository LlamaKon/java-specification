package second;

public class Sample5 {

    public static void main(String[] args) {

        // 変数定義
        String s1, s2, s3, s4;

        /*
        文字列の演算は足し算のみ可能
         */

        // 文字列代入
        s1 = "ABC";
        s2 = "DEF";
        s3 = s1 + s2;

        // 出力
        System.out.println(s1);
        System.out.println(s2);
        System.out.println(s1 + s2);
        System.out.println(s3);


    }
}

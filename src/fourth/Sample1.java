package fourth;

public class Sample1 {

    public static void main(String[] args) {

        /** for文
         * int i = 1 → カウンタ(int)の初期値を設定
         * i <= 5 → 繰り返しを続ける条件
         * i++ → カウンタの更新
         */

        for (int i = 1; i <= 5; i++) {
            System.out.print(i + " ");
        }
        System.out.println();
    }
}
